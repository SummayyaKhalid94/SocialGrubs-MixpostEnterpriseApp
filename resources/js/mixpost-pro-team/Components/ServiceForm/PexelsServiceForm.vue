<script setup>
    import {ref} from "vue";
    import {useI18n} from "vue-i18n";
    import {router} from "@inertiajs/vue3";
    import useNotifications from "@p/Composables/useNotifications";
    import Panel from "@p/Components/Surface/Panel.vue";
    import Input from "@p/Components/Form/Input.vue";
    import PexelsIcon from "@p/Icons/Pexels.vue";
    import PrimaryButton from "@p/Components/Button/PrimaryButton.vue";
    import Error from "@p/Components/Form/Error.vue";
    import Checkbox from "../Form/Checkbox.vue";
    import Flex from "../Layout/Flex.vue";
    import Label from "../Form/Label.vue";
    import LabelSuffix from "../Form/LabelSuffix.vue";
    import HorizontalGroup from "../Layout/HorizontalGroup.vue";
    import ReadDocHelp from "@p/Components/Util/ReadDocHelp.vue";

    const {t: $t} = useI18n()

    const props = defineProps({
        form: {
            required: true,
            type: Object
        }
    })

    const {notify} = useNotifications();
    const errors = ref({});

    const save = () => {
        errors.value = {};

        router.put(route('mixpost.services.update', {service: 'pexels'}), props.form, {
            preserveScroll: true,
            onSuccess() {
                notify('success', $t('service.service_saved', {service: 'pexels'}));
            },
            onError: (err) => {
                errors.value = err;
            },
        });
    }
</script>
<template>
    <Panel>
        <template #title>
            <div class="flex items-center">
                <span class="mr-xs">
                    <PexelsIcon/>
                </span>
                <span>Pexels</span>
            </div>
        </template>

        <template #description>
            <p>{{ $t('service.stock_photo.can_use_external_photos', {provider: 'Pexels'}) }}</p>
            <p><a href="https://www.pexels.com/api/key" class="link" target="_blank">{{ $t('service.generate_api_key') }}</a>.</p>
            <ReadDocHelp :href="`${$page.props.mixpost.docs_link}/services/media/pexels`"
                         class="mt-xs"/>
        </template>

        <HorizontalGroup class="mt-lg">
            <template #title>
                <label for="client_id">API Key <LabelSuffix danger>*</LabelSuffix></label>
            </template>

            <Input v-model="form.configuration.client_id"
                   :error="errors['configuration.client_id'] !== undefined"
                   type="text"
                   id="client_id"
                   autocomplete="off"/>

            <template #footer>
                <Error :message="errors['configuration.client_id']"/>
            </template>
        </HorizontalGroup>

        <HorizontalGroup class="mt-lg">
            <template #title>
                {{ $t('general.status') }}
            </template>

            <Flex :responsive="false" class="items-center">
                <Checkbox v-model:checked="form.active" id="active"/>
                <Label for="active" class="mb-0!">{{ $t('general.active') }}</Label>
            </Flex>

            <template #footer>
                <Error :message="errors.active"/>
            </template>
        </HorizontalGroup>

        <PrimaryButton @click="save" class="mt-lg">{{ $t('general.save') }}</PrimaryButton>
    </Panel>
</template>

<script setup>
import {inject, ref} from "vue";
import {router, useForm} from "@inertiajs/vue3";
import {Head} from "@inertiajs/vue3";
import {useI18n} from "vue-i18n";
import useNotifications from "@p/Composables/useNotifications";
import PageHeader from "@p/Components/DataDisplay/PageHeader.vue";
import Panel from "@p/Components/Surface/Panel.vue";
import Modal from "@p/Components/Modal/Modal.vue";
import ConfirmationModal from "@p/Components/Modal/ConfirmationModal.vue";
import Account from "@p/Components/Account/Account.vue";
import AddTwitterAccount from "@p/Components/Account/AddTwitterAccount.vue";
import AddFacebookPage from "@p/Components/Account/AddFacebookPage.vue";
import AddMastodonAccount from "@p/Components/Account/AddMastodonAccount.vue";
import AddInstagramAccount from "@p/Components/Account/AddInstagramAccount.vue";
import AddThreadsAccount from "@p/Components/Account/AddThreadsAccount.vue";
import AddYoutubeAccount from "@p/Components/Account/AddYoutubeAccount.vue";
import AddGBPAccount from "@p/Components/Account/AddGBPAccount.vue";
import AddPinterestAccount from "@p/Components/Account/AddPinterestAccount.vue";
import AddLinkedinProfile from "@p/Components/Account/AddLinkedinProfile.vue";
import AddLinkedinPage from "@p/Components/Account/AddLinkedinPage.vue";
import AddTikTokAccount from "@p/Components/Account/AddTikTokAccount.vue";
import AddBlueskyAccount from "@p/Components/Account/AddBlueskyAccount.vue";
import SecondaryButton from "@p/Components/Button/SecondaryButton.vue";
import DangerButton from "@p/Components/Button/DangerButton.vue";
import Dropdown from "@p/Components/Dropdown/Dropdown.vue";
import DropdownItem from "@p/Components/Dropdown/DropdownItem.vue";
import GridAddElement from "@p/Components/DataDisplay/GridAddElement.vue";
import EllipsisVerticalIcon from "@p/Icons/EllipsisVertical.vue";
import RefreshIcon from "@p/Icons/Refresh.vue";
import TrashIcon from "@p/Icons/Trash.vue";
import PrimaryButton from "@p/Components/Button/PrimaryButton.vue";
import PureButton from "@p/Components/Button/PureButton.vue";
import PencilSquare from "@p/Icons/PencilSquare.vue";
import DialogModal from "@p/Components/Modal/DialogModal.vue";
import Input from "@p/Components/Form/Input.vue";
import AlertUnconfiguredService from "@p/Components/Util/AlertUnconfiguredService.vue";
import Alert from "@p/Components/Util/Alert.vue";
import Flex from "@p/Components/Layout/Flex.vue";
import useRequestErrorsHandler from "@p/Composables/useRequestErrorsHandler.js";
import ArrowTopRightOnSquare from "@p/Icons/ArrowTopRightOnSquare.vue";

const {t: $t} = useI18n()

const workspaceCtx = inject("workspaceCtx");
const routePrefix = inject('routePrefix');

const title = $t("account.accounts");

const {notify} = useNotifications();

const addAccountModal = ref(false);
const confirmationAccountDeletion = ref(null);
const accountIsDeleting = ref(false);

const updateAccount = (accountUuid) => {
    router.put(
        route("mixpost.accounts.update", {
            workspace: workspaceCtx.id,
            account: accountUuid,
        }),
        {},
        {
            preserveScroll: true,
            onSuccess(response) {
                if (response.props.flash.error) {
                    return;
                }

                notify("success", $t("account.account_updated"));
            },
        }
    );
};

const deleteAccount = () => {
    router.delete(
        route(`${routePrefix}.accounts.delete`, {
            workspace: workspaceCtx.id,
            account: confirmationAccountDeletion.value,
        }),
        {
            preserveScroll: true,
            onStart() {
                accountIsDeleting.value = true;
            },
            onSuccess() {
                confirmationAccountDeletion.value = null;
                notify("success", $t("account.account_deleted"));
            },
            onFinish() {
                accountIsDeleting.value = false;
            },
        }
    );
};

const closeConfirmationAccountDeletion = () => {
    if (accountIsDeleting.value) {
        return;
    }

    confirmationAccountDeletion.value = null;
};

const showSuffixForm = ref(false);
const suffixForm = useForm({
    uuid: null,
    name: "",
});

// Account Suffix
const openSuffixForm = (account) => {
    showSuffixForm.value = true;
    suffixForm.uuid = account.uuid;
    suffixForm.name = account.suffix;
};

const closeSuffixForm = () => {
    showSuffixForm.value = null;
    suffixForm.reset();
};

const updateSuffix = () => {
    suffixForm.put(
        route(`${routePrefix}.accounts.updateSuffix`, {
            workspace: workspaceCtx.id,
            account: suffixForm.uuid,
        }),
        {
            onSuccess() {
                closeSuffixForm();
            },
        }
    );
};

useRequestErrorsHandler();
</script>
<template>
    <Head :title="title"/>

    <div class="w-full mx-auto row-py">
        <PageHeader :title="title">
            <template #description>
                {{ $t("account.connect_social_account") }}
            </template>
        </PageHeader>
        <div class="mt-lg row-px w-full">
            <AlertUnconfiguredService
                :isConfigured="$page.props.is_configured_service"
            />

            <template
                v-if="!$page.props.is_service_active.twitter && $page.props.enterprise_config.workspace_twitter_service">
                <div class="mb-md">
                    <Alert variant="warning" :closeable="false">
                        <p>{{ $t('service.not_configured_service', {service: 'X'}) }}</p>
                    </Alert>

                    <Flex>
                        <a :href="`/${$page.props.mixpost.core_path}/${workspaceCtx.id}/workspace-services?redirect-to-accounts=true`"
                           class="mt-md block">
                            <PrimaryButton>{{ $t('service.configure_services') }}</PrimaryButton>
                        </a>
                    </Flex>
                </div>
            </template>

            <div
                class="w-full grid grid-cols-1 md:grid-cols-2 xl:grid-cols-4 2xl:grid-cols-5 gap-6"
            >
                <button @click="addAccountModal = true">
                    <GridAddElement>
                        {{ $t("account.add_account") }}
                    </GridAddElement>
                </button>
                <template
                    v-for="account in $page.props.accounts"
                    :key="account.id"
                >
                    <Panel class="relative">
                        <div class="flex flex-col justify-center relative">
                            <Account
                                size="lg"
                                :img-url="account.image"
                                :provider="account.provider"
                                :name="account.name"
                                :active="true"
                            />

                            <div
                                v-if="!account.authorized"
                                class="absolute top-0 left-0"
                            >
                                <div
                                    v-tooltip="$t('account.unauthorized')"
                                    class="w-md h-md bg-red-500 rounded-full"
                                ></div>
                            </div>

                            <div
                                class="mt-sm font-medium text-center break-words"
                            >
                                {{ account.name }}
                            </div>
                            <div class="mt-1 text-center text-stone-800">
                                {{ $t("account.added") }}
                                {{ account.created_at }}
                            </div>
                        </div>

                        <div class="absolute top-0 right-0 mt-sm mr-sm">
                            <Dropdown widthClasses="56">
                                <template #trigger>
                                    <PureButton>
                                        <EllipsisVerticalIcon/>
                                    </PureButton>
                                </template>
                                <template #content>
                                    <DropdownItem
                                        v-if="account.external_account_url"
                                        as="a"
                                        :href="account.external_account_url"
                                        target="_blank"
                                    >
                                        <template #icon>
                                            <ArrowTopRightOnSquare/>
                                        </template>
                                        {{ $t('account.open_account_page') }}
                                    </DropdownItem>
                                    <DropdownItem
                                        @click="updateAccount(account.uuid)"
                                        as="button"
                                    >
                                        <template #icon>
                                            <RefreshIcon/>
                                        </template>
                                        {{ $t("general.update") }}
                                    </DropdownItem>
                                    <DropdownItem
                                        @click="openSuffixForm(account)"
                                        as="button"
                                    >
                                        <template #icon>
                                            <PencilSquare/>
                                        </template>
                                        {{ $t("account.edit_suffix") }}
                                    </DropdownItem>
                                    <DropdownItem
                                        @click="
                                            confirmationAccountDeletion =
                                                account.uuid
                                        "
                                        as="button"
                                    >
                                        <template #icon>
                                            <TrashIcon class="text-red-500"/>
                                        </template>
                                        {{ $t("general.delete") }}
                                    </DropdownItem>
                                </template>
                            </Dropdown>
                        </div>
                    </Panel>
                </template>
            </div>
        </div>
    </div>

    <DialogModal
        :show="showSuffixForm"
        max-width="sm"
        :closeable="true"
        :scrollable-body="true"
        @close="closeSuffixForm"
    >
        <template #header>
            {{ $t("account.edit_account_suffix") }}
        </template>

        <template #body>
            <div class="mt-xs">
                <Input
                    type="text"
                    v-model="suffixForm.name"
                    :placeholder="$t('account.enter_suffix')"
                />
            </div>
        </template>

        <template #footer>
            <SecondaryButton @click="closeSuffixForm" class="mr-xs">{{
                    $t("general.close")
                }}
            </SecondaryButton>
            <PrimaryButton
                @click="updateSuffix"
                :isLoading="suffixForm.processing"
                :disabled="suffixForm.processing"
            >
                {{ $t("general.save") }}
            </PrimaryButton>
        </template>
    </DialogModal>

    <ConfirmationModal
        :show="confirmationAccountDeletion !== null"
        @close="closeConfirmationAccountDeletion"
        variant="danger"
    >
        <template #header>{{ $t("account.delete_account") }}</template>
        <template #body>
            {{ $t("account.confirm_delete_account") }}
        </template>
        <template #footer>
            <SecondaryButton
                @click="closeConfirmationAccountDeletion"
                :disabled="accountIsDeleting"
                class="mr-xs"
            >
                {{ $t("general.cancel") }}
            </SecondaryButton>
            <DangerButton
                @click="deleteAccount"
                :is-loading="accountIsDeleting"
                :disabled="accountIsDeleting"
            >
                {{ $t("general.delete") }}
            </DangerButton>
        </template>
    </ConfirmationModal>

    <Modal
        :show="addAccountModal"
        :closeable="true"
        @close="addAccountModal = false"
    >
        <div class="flex flex-col">
            <AddFacebookPage
                v-if="$page.props.is_service_active.facebook"
            />
            <AddInstagramAccount
                v-if="$page.props.is_service_active.facebook"
            />
            <AddThreadsAccount
                v-if="$page.props.is_service_active.threads"
            />
            <AddMastodonAccount/>
            <AddBlueskyAccount
                v-if="$page.props.is_service_active.bluesky"/>
            <AddYoutubeAccount
                v-if="$page.props.is_service_active.google"
            />
            <AddGBPAccount
                v-if="$page.props.is_service_active.google"
            />
            <AddPinterestAccount
                v-if="$page.props.is_service_active.pinterest"
            />
            <AddLinkedinProfile
                v-if="$page.props.is_service_active.linkedin"
            />
            <AddLinkedinPage
                v-if="
                    $page.props.is_service_active.linkedin &&
                    $page.props.additionally.linkedin_supports_pages
                "
            />
            <AddTikTokAccount v-if="$page.props.is_service_active.tiktok"/>
            <AddTwitterAccount
                v-if="$page.props.is_service_active.twitter"
            />
        </div>
    </Modal>
</template>
